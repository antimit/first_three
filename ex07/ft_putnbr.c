#include <unistd.h>

void	ft_putnbr(int nb)
{
	int		d;
	char	c;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
	}
	d = 1000000000;
	while (d > 0)
	{
		if (nb / d != 0 || d == 1)
		{
			c = '0' + (nb / d % 10);
			write(1, &c, 1);
		}
		d /= 10;
	}
}
